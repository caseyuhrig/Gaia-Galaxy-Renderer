    @SafeVarargs
    public static Optional<RGBA8> average(final Optional<RGBA8>... colors) {
        double r = 0;
        double g = 0;
        double b = 0;
        double a = 0;
        double n = 0;
        for (final var color : colors) {
            if (color.isPresent()) {
                r += color.get().red;
                g += color.get().green;
                b += color.get().blue;
                a += color.get().alpha;
            }
            n++;
        }
        if (n == 0) return Optional.empty();
        return Optional.of(new RGBA8((int) Math.round(r / n), (int) Math.round(g / n), (int) Math.round(b / n), (int) Math.round(a / n)));
    }
